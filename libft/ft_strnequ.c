#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;

	if (s1 && s2)
	{
		i = ft_strncmp(s1, s2, n);
		if (!i)
			return (1);
	}
	return (0);
}
