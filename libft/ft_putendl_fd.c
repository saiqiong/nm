#include <unistd.h>
#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char	c;

	c = '\n';
	if (s)
	{
		while (*s)
			write(fd, s++, 1);
		write(fd, &c, 1);
	}
}
